					<h5>Step 3: Call the ubislider</h5>
					<p>Call .ubislider() on &#60;div id="ubislider">. Note that the call can made inside or outside of a $(document).ready() call, and the  plugin will work without issue! </p>
<h6>Example 1 code | Horizontal</h6>
<p></p>
<pre><code>$(document).ready(function(){
  $('#slider').ubislider({
	arrowsToggle: true,
        type: 'ecommerce',
        hideArrows: true,
        autoSlideOnLastClick: true,
        modalOnClick: true
  });
});
</code></pre>
<h6>Example 2 & 3 code | Vertical on the left and right</h6>
<p>Need to assign position to vertical so slide with start scrolling from Top to bottom inside of standard scrolling.</p>
<pre><code>$(document).ready(function(){
 $('#slider').ubislider({
    arrowsToggle: true,
    type: 'ecommerce',
    hideArrows: true,
    autoSlideOnLastClick: true,
    modalOnClick: true,
    position: 'vertical'
 }); 
});
</code></pre>
<h6>Example 4 code | Integration with ElevateZoom</h6>
<p>You can call onTopImageChange callback function for running scripts after the top image is changed.</p>
<pre><code>$(document).ready(function(){
 $('#slider').ubislider({
    arrowsToggle: true,
    type: 'ecommerce',
    hideArrows: true,
    autoSlideOnLastClick: true,
    modalOnClick: true,
    onTopImageChange: function(){
    	$('#imageSlider img').elevateZoom();
    }
 }); 
});
</code></pre>

