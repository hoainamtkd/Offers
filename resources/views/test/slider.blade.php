					<h4>Installation</h4>
					<h5>Step 1: Link required files</h5>
					<p>First and most important, the jQuery library needs to be included (no need to download - link directly from Google). Next, download the package from this site and link the ubislider CSS file (for the theme) and the ubislider Javascript file.</p>
					<pre><code>&#60;script src="https://ajax.googleapis.com/ajax/libs/jquery/1.11.3/jquery.min.js">&#60;/script><Br /><i>&#60;!-- ubislider Javascript file --></i><Br />&#60;script src="/js/ubislider.min.js">&#60;/script><i><Br />&#60;!-- ubislider CSS file --></i><Br />&#60;link href="/css/ubislider.css" rel="stylesheet" /></code></pre>
					<h5>Step 2: Create HTML markup</h5>
					<p>Slides can contain images, video, or any other HTML content! Put your content of each slider inside &#60;li> tag</p>
					<h6>Example 1 and 4 code | Horizontal and Integration with ElevateZoom</h6>
					<p>For integration with ElevateZoon specify id to .ubislider-image-container div.</p>
<pre><code>&#60;div class="ubislider-image-container" data-ubislider="#slider" id="imageSlider">&#60;/div>
&#60;div class="ubislider" id="slider">
